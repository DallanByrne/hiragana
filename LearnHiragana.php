		<h1>Welcome to Learn Hiragana</h1>
		<hr>
		
		<p>The hiragana alphabet is one of the three writing systems used in Japenese. It is generally used in topic markers, as context for a kanji character
		and nouns without any kanji equivalent. It is part of the Kana system of writing. This is a syllabic writting system, which means each character 
		represents a syllable.</p>
		
		<p>
			Hiragana developed from the simplificatin of the  man'yōgana writing system. This was the first known kana system in Japanese history, and
			 was heavily influenced by the Chinese kanji writing of the time. The system was gradualy changed into contemporary hiragana. However some more recent changes 
			 in the hiragana format is the removal of <b>ゐ (wi)</b> and <b>ゑ (we)</b>. These characters have become obsolete due to pronounciations being similar to their voule counterparts.
		</p>
		
		<p>
			The table below has all modern day Hiragana characters, with their associated pronunciation. Take your time learning the stroke order and romaji
			 of each element. Then try out our quiz to test your knowledge on hiragana.
		</p>
		
